#include "fillit.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	display_square(t_list *list)
{
	t_node	*tmp;

	tmp = list->head;
	while (tmp)
	{
		ft_putchar(tmp->data);
		tmp = tmp->next;
	}
}

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}
